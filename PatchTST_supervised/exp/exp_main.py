    def predict(self, setting, checkpoint_path, load=False):
        pred_data, pred_loader = self._get_data(flag='pred')

        if load:
            path = os.path.join(checkpoint_path, 'checkpoint.pth')
            self.model.load_state_dict(torch.load(path))

        preds = []

        self.model.eval()
        max_len = len(pred_loader)
        with torch.no_grad():
            start_time = time.time()  # 记录 forward 开始时间
            for i, (batch_x, batch_y, batch_x_mark, batch_y_mark) in enumerate(pred_loader):
                f_dim = -1 if self.args.features == 'MS' else 0
                batch_x = batch_x.float().to(self.device)
                batch_y = batch_y.float()
                batch_x_mark = batch_x_mark.float().to(self.device)
                batch_y_mark = batch_y_mark.float().to(self.device)

                # decoder input
                dec_inp = torch.zeros([batch_y.shape[0], self.args.pred_len, batch_y.shape[2]]).float().to(batch_y.device)
                dec_inp = torch.cat([batch_y[:, :self.args.label_len, :], dec_inp], dim=1).float().to(self.device)
                # encoder - decoder
                if self.args.use_amp:
                    with torch.cuda.amp.autocast():
                        if 'Linear' in self.args.model or 'TST' in self.args.model:
                            outputs = self.model(batch_x)
                        else:
                            if self.args.output_attention:
                                outputs = self.model(batch_x, batch_x_mark, dec_inp, batch_y_mark)[0]
                            else:
                                outputs = self.model(batch_x, batch_x_mark, dec_inp, batch_y_mark)
                else:
                    if 'Linear' in self.args.model or 'TST' in self.args.model:
                        outputs = self.model(batch_x)
                    else:
                        if self.args.output_attention:
                            outputs = self.model(batch_x, batch_x_mark, dec_inp, batch_y_mark)[0]
                        else:
                            outputs = self.model(batch_x, batch_x_mark, dec_inp, batch_y_mark)
                
                # outputs = outputs[:, -self.args.pred_len:, f_dim:]
                pred = outputs.detach().cpu().numpy()
                pred = pred.squeeze(0)
                pred = pred_data.scaler.inverse_transform(pred)
                pred = pred[-self.args.pred_len:, f_dim:]
                preds.append(pred)
        end_time = time.time()  # 记录 forward 结束时间
        total_forward_time = end_time - start_time  # 计算本次 forward 时间
        print(total_forward_time)
        preds = np.array(preds)
        preds = preds.reshape(-1, preds.shape[-2], preds.shape[-1])
        preds = preds.squeeze()

        folder_path = './results/' + setting + '/'
        if not os.path.exists(folder_path):
            os.makedirs(folder_path)
        
        if self.args.draw == 1:
            preds = preds[:-1]
            # 获取原始数据
            original_data = pred_data.scaler.inverse_transform(pred_data.data_x[self.args.seq_len:self.args.seq_len + len(preds)])  # 反归一化原始数据
            original_data = original_data[:, -1]
            
            # 计算 MSE 和 MAE
            mse = mean_squared_error(original_data, preds)
            mae = mean_absolute_error(original_data, preds)
            
            # 绘制折线图
            plt.figure(figsize=(12, 6))
            plt.plot(original_data, label='Original Data', color='blue')
            plt.plot(preds, label='Predicted Data', color='red')
            plt.text(0.5, 0.02, f'MSE: {mse:.8f}, MAE: {mae:.8f}', transform=plt.gca().transAxes, fontsize=12, horizontalalignment='center', verticalalignment='bottom', bbox=dict(boxstyle='round', facecolor='white', alpha=0.5))
            plt.xlabel('Time Steps')
            plt.ylabel('Value')
            plt.title('Original vs Predicted Values Over Time')
            plt.legend()
            plt.savefig(os.path.join(checkpoint_path, 'pred_s_sin.png'), dpi=300)
            
        else:
            print(preds)
        np.save(folder_path + 'real_prediction.npy', preds)

        return
